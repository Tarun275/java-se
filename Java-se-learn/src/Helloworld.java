public class Helloworld 
{
    public static void main(String[] args)
    {
        System.out.println("Welcome to JAVA! Let's do the coding.");
        
        // multiline print
        System.out.print("Welcome to JAVA! ");
        System.out.println("Let's do the coding.");
        System.out.print("Welcome to JAVA! ");
        System.out.println("Let's do the coding.");
        
        // multiline print in one line
        System.out.println("Welcome to JAVA!\nLet's do the coding.");
        
        // formatted print
        System.out.printf("%s%n%s%n", "Welcome to JAVA", "Let's do the coding.");
        
    }
}
